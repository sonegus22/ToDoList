/**
 * This method send a task to the server to store it in the database
 */
function sendTask(){

    //let result = document.querySelector('.result');
    let name = document.querySelector('#myInput');

    // create an XHR request
    let xhr = new XMLHttpRequest();
    let url = 'http://localhost/ToDoList/pages/php/controller/controller.php&controller=task&action=addTask';

    // open a connection
    xhr.open('POST', url, true);

    // set the request header
    xhr.setRequestHeader('Content-Type', 'application/json');

    // send the request
    var data = JSON.stringify({"name": name.value});
    xhr.send(data);

}